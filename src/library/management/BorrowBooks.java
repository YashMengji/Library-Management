    private void borrowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrowButtonActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost/library";//This is url for sql
        String user = "root";
        String pwd = "123";
        String input = borrowText.getText();
        //Below query deletes entry from available books table 
        String removeQuery = "delete from books where book_id='"+input+"' or name='"+input+"';";
        //Below query adds entry from availale books table to issued books table
        String issueQuery = "INSERT INTO `ISSUED BOOKS` (`BOOK ID`, `CATEGORY`, `NAME`, `AUTHOR`, `COPIES`) " +
                   "SELECT `BOOK_ID`, `CATEGORY`, `NAME`, `AUTHOR`, `COPIES` " +
                   "FROM `BOOKS` " +
                   "WHERE BOOK_ID = '"+input+"' OR NAME = '"+input+"';";

        try{
            Connection conn = DriverManager.getConnection(url, user, pwd);
            Statement stmnt = conn.createStatement();
            stmnt.executeUpdate(issueQuery);
            int rows = stmnt.executeUpdate(removeQuery); //How many rows are being affected by removeQuery is stored in variable rows                             
            if(rows>0){
                JOptionPane.showMessageDialog(this, "Book removed from library successfully");
            }
            else{
                JOptionPane.showMessageDialog(this, "No such book available");
            }
            stmnt.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_borrowButtonActionPerformed

