    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost/library";
        String user = "root";
        String pwd = "123";
        String query = "insert into books values(?,?,?,?,?)"; //Creates a new row hence taken question marks.
        String id = idText.getText();
        String category = catgText.getText();
        String name = nameText.getText();
        String author = authText.getText();
        int copies = Integer.parseInt(copyText.getText());
        //Below is the check query which checks where each content except id is same and adds the book's copies no. to existing row.
        String checkquery = "update books set copies=copies+"+copies+" where name='"+name+"' and category='"+category+"' and author='"+author+"';";
        try{
            Connection conn = DriverManager.getConnection(url, user, pwd);
            Statement stmnt = conn.createStatement();
            //Below returns how many rows are affected by checkquery.
            //We store it in rows and if it is nonZero then we will not execute else parts command of creating new row
            int rows = stmnt.executeUpdate(checkquery);
            if(rows>0){
                JOptionPane.showMessageDialog(this, "One record added successfully");
            }
            else{
                PreparedStatement stm = conn.prepareCall(query);
                stm.setString(1, id);
                stm.setString(2, category);
                stm.setString(3, name);
                stm.setString(4, author);
                stm.setInt(5, copies);
                stm.execute();
                JOptionPane.showMessageDialog(this, "One record added successfully");
            }
            idText.setText(null);
            catgText.setText(null);
            nameText.setText(null);
            authText.setText(null);
            copyText.setText(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

